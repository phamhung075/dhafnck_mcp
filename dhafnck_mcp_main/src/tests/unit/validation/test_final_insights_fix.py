def test_actual_subtask_controller():
    """Test the actual subtask controller that has the monkey patch applied."""
    print("🧪 Testing Actual Subtask Controller with Schema Monkey Patch")
    print("=" * 70)
    
    try:
        # Apply monkey patch first
        from fastmcp.task_management.interface.utils.schema_monkey_patch import apply_all_schema_patches
        apply_all_schema_patches()
        print("✅ Schema monkey patches applied")
        
        # Import FastMCP server class
        from fastmcp.server.server import FastMCP
        
        # Create a test FastMCP instance
        mcp = FastMCP(name="test_server")
        
        # Import the actual subtask controller
        from fastmcp.task_management.interface.controllers.subtask_mcp_controller import SubtaskMCPController
        from fastmcp.task_management.application.factories.subtask_facade_factory import SubtaskFacadeFactory
        from fastmcp.task_management.infrastructure.repositories.subtask_repository_factory import SubtaskRepositoryFactory
        
        # Create the factory with proper dependencies
        subtask_repository_factory = SubtaskRepositoryFactory()
        subtask_facade_factory = SubtaskFacadeFactory(subtask_repository_factory)
        
        # Create controller
        controller = SubtaskMCPController(subtask_facade_factory)
        
        # Register the tools (this will apply the monkey patch during registration)
        controller.register_tools(mcp)
        print("✅ Subtask tools registered with monkey patch applied")
        
        # Get the registered tools
        tools = mcp.list_tools()
        manage_subtask_tool = None
        
        for tool in tools:
            if tool.name == "manage_subtask":
                manage_subtask_tool = tool
                break
        
        if not manage_subtask_tool:
            print("❌ manage_subtask tool not found")
            return False
        
        # Check the schema for insights_found parameter
        schema = manage_subtask_tool.inputSchema
        insights_schema = schema.get("properties", {}).get("insights_found", {})
        
        print(f"\n📋 Schema for insights_found parameter:")
        print(json.dumps(insights_schema, indent=2))
        
        # Analyze the schema
        has_any_of = "anyOf" in insights_schema
        schema_allows_string = False
        schema_allows_array = False
        
        if has_any_of:
            print("\n🔍 Schema Analysis:")
            for i, option in enumerate(insights_schema["anyOf"]):
                print(f"  Option {i+1}: {option}")
                if option.get("type") == "string":
                    schema_allows_string = True
                elif option.get("type") == "array":
                    schema_allows_array = True
        else:
            # Single type schema
            schema_type = insights_schema.get("type")
            if schema_type == "string":
                schema_allows_string = True
            elif schema_type == "array":
                schema_allows_array = True
        
        schema_is_flexible = schema_allows_string and schema_allows_array
        
        print(f"\n📊 Schema Flexibility Assessment:")
        print(f"   Allows string: {schema_allows_string}")
        print(f"   Allows array: {schema_allows_array}")
        print(f"   Is flexible: {schema_is_flexible}")
        
        if schema_is_flexible:
            print("✅ SUCCESS: Schema is flexible and should accept JSON string arrays!")
        else:
            print("❌ FAILED: Schema is not flexible enough")
        
        return schema_is_flexible
        
    except Exception as e:
        print(f"❌ Error in subtask controller test: {e}")
        import traceback
        traceback.print_exc()
        return False


